export function translateCanChi(ganzhi: string) {
  if (!ganzhi || ganzhi.length < 2) return ganzhi;
  const gan = THIEN_CAN[ganzhi.charAt(0)] || ganzhi.charAt(0);
  const zhi = DI_CHI[ganzhi.charAt(1)] || ganzhi.charAt(1);
  return `${gan} ${zhi}`;
}

export function translateZodiac(zodiac: string) {
  return ZODIAC[zodiac] || zodiac;
}

export function translateDirection(dir: string) {
  return DIRECTION[dir] || dir;
}

export function translateJieQi(jieQi: string) {
  if (!jieQi) return '';
  return JIE_QI[jieQi] || jieQi;
}

export function translateZhiXing(zhiXing: string) {
  return ZHI_XING[zhiXing] || zhiXing;
}

export function translateXiu(xiu: string) {
  return XIU[xiu] || xiu;
}

export function translateYueXiang(yueXiang: string) {
  // Sometimes it returns just '朔' instead of '朔月'
  const key = yueXiang.endsWith('月') ? yueXiang : yueXiang + '月';
  return YUE_XIANG[key] || yueXiang;
}

export function translateYiJi(activity: string) {
  return YI_JI[activity] || activity;
}
